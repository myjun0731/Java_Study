public class Main {
    public static void main(String[] args) {
        System.out.println("Hello, World!");

        String[] names = {"Ava", "Ben", "Cora"};
        System.out.println("First name: " + names[0]);

        for (int i = 0; i < names.length; i++) {
            System.out.println("Name " + i + ": " + names[i]);
        }
    }
}
